export function getProductBySlug(slug: string, gender?: "homme" | "femme"): Product | undefined {
  return products.find((p) => p.slug === slug && (gender ? p.gender === gender : true));
}

export function getSimilarProducts(currentProductId: number, category: string, gender: "homme" | "femme", limit = 4): Product[] {
  return products
    .filter((p) => p.id !== currentProductId && p.category === category && p.gender === gender)
    .slice(0, limit);
}